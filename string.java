public class string {
	public static void main(String args[]){
		String txt = " Hello Programmer ";
		String txt2 = "mango";
		
		System.out.println("The length of txt: " + txt.length());
		System.out.println("lowercase: " +  txt.toLowerCase());
		System.out.println("uppercase: " + txt.toUpperCase());
		System.out.println("index of hello: " + txt.indexOf("Hello"));
		System.out.println("char at index 4: " + txt.charAt(4));	
		System.out.println("txt and txt2 equals? Ans: " + txt.equals(txt2));
		System.out.println("trimmed txt: " + txt.trim());
	}
    

}
