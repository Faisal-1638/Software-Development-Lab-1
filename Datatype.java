public class Datatype {
    public static void main(String args[]){
		byte myNum = 127;
		System.out.println("the byte type integer is: " + myNum);

		float floatVal = 3e3f;
		double doubleVal = 44e4d;

		System.out.println(floatVal);
		System.out.println(doubleVal);

		boolean areYouCrying = false;
		boolean areYouLying = true;

		System.out.println("are you crying? " + areYouCrying);
		System.out.println("are you lying? " + areYouLying);

		char myGrade = 'A';

		System.out.println(myGrade);

		var anotherNum = 21;
		System.out.println(anotherNum);

		String endMsg = "Thank you";
		System.out.println(endMsg);
	}
    
}
